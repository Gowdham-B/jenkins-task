# Function definition
def add_numbers(a, b):
    result = a + b
    return result

# Taking input from user
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))

# Function call
sum_result = add_numbers(num1, num2)

# Output
print("The sum of num1 and num2 is:", sum_result)
